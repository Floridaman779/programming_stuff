should_repeat = True

def caesar(direction, shift, text):
    modded_text = ""
    for letter in text:
        if letter in alphabet2:
            if direction == "encode":
                position = alphabet.index(letter) + shift
                if position >= len(alphabet):
                    position = position % len(alphabet)
                
            elif direction == "decode":
                position = alphabet.index(letter) - shift
                if position < 0:
                    position = position % len(alphabet)   
            letter = alphabet[position]
            modded_text += letter
        else:
            modded_text += letter
            
    print(f"Your {direction}d text is: {modded_text}")

